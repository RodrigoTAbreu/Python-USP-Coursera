primeira_nota = int(input("Digite a primeira nota:"))
segunda_nota = int(input("Digite a segunda nota:"))
terceira_nota = int(input("Digite a terceira nota:"))
quarta_nota = int(input("Ditie a quarta nota:"))
media = (primeira_nota + segunda_nota + terceira_nota + quarta_nota) / 4
print("A média aritmética é {}".format(media))